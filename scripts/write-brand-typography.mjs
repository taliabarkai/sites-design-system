const px = (n) => `${n / 16}rem`;
const lsPx = (n) => `${n / 16}rem`;

function rule({
  fontFamily,
  sizePx,
  lhPx,
  mbSizePx,
  mbLhPx,
  fontWeight = '400',
  letterSpacing,
  letterSpacingMb,
  textTransform,
  textDecoration,
}) {
  const o = {
    fontFamily,
    fontSize: px(sizePx),
    lineHeight: px(lhPx),
    fontWeight: String(fontWeight),
  };
  if (letterSpacing != null) o.letterSpacing = lsPx(letterSpacing);
  if (textTransform) o.textTransform = textTransform;
  if (textDecoration) o.textDecoration = textDecoration;

  const ms = mbSizePx ?? sizePx;
  const ml = mbLhPx ?? lhPx;
  if (ms !== sizePx || ml !== lhPx) {
    o.fontSizeMobile = px(ms);
    o.lineHeightMobile = px(ml);
  }
  if (
    letterSpacingMb != null &&
    letterSpacing != null &&
    letterSpacingMb !== letterSpacing
  ) {
    o.letterSpacingMobile = lsPx(letterSpacingMb);
  }
  return o;
}

